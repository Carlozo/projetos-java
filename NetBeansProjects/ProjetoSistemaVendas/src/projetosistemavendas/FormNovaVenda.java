    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            this.venda.setNumero(Integer.parseInt(jTextField1.getText()));
            this.venda.setData(jTextField2.getText());
            this.venda.setCliente().setCpf(this.listaCliente.get(jComboBox1.getSelectedIndex()).getCpf());
            this.venda.setCliente().setNome(this.listaCliente.get(jComboBox1.getSelectedIndex()).getNome());
            RepositorioVendaODBC.obterInstancia().inserir(venda);
            JOptionPane.showMessageDialog(null, "Venda cadastrada com sucesso");
            this.dispose();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

